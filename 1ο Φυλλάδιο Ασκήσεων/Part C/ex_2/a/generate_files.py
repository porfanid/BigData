# Εισάγουμε τη βιβλιοθήκη random για να δημιουργήσουμε τυχαίες τιμές
import random

def random_bitstring(length=100):
    """
    Δημιουργεί τυχαίες συμβολοσειρές από bits (0 και 1).

    Παράμετροι:
    -----------
    length : int, προαιρετικό
        Το μήκος της συμβολοσειράς (προεπιλογή: 100).

    Επιστρέφει:
    -----------
    str
        Μια τυχαία συμβολοσειρά από '0' και '1' χαρακτήρες.
    """
    # Για κάθε θέση από 0 μέχρι length-1, επιλέγουμε τυχαία έναν χαρακτήρα '0' ή '1' και ενώνουμε τα αποτελέσματα
    return ''.join(random.choice('01') for _ in range(length))


# --- Δημιουργία των κοινών αρχείων ---

# Δημιουργούμε μια λίστα με 10 bitstrings, που θα είναι κοινά και στα δύο σύνολα
common_files = [random_bitstring() for _ in range(10)]


# --- Δημιουργία συνόλων αρχείων A και B ---

# Το σύνολο A περιλαμβάνει:
# - τα 10 κοινά αρχεία (ακριβώς ίδια με του B)
# - 99990 μοναδικά αρχεία (τυχαίες bitstrings)
A_files = common_files + [random_bitstring() for _ in range(99990)]

# Το σύνολο B περιλαμβάνει:
# - τα ίδια 10 κοινά αρχεία
# - επίσης 99990 διαφορετικά τυχαία αρχεία
B_files = common_files + [random_bitstring() for _ in range(99990)]
